function abc (argumento){
    var primeira = argumento.substr(0,1);
    if (primeira == "a")
        return (true);
    if (primeira == "b")
        return (true);
    if (primeira == "c")
        return (true);
    if (primeira == "A")
        return (true);
    if (primeira == "B")
        return (true);
    if (primeira == "C")
        return (true);
    else {
        return (false);
    }
}