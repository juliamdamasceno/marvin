function alfabetoMilitar (vogal){
    if (vogal == "a")
        return ("alpha");
    if (vogal == "e")
        return ("echo");
    if (vogal == "i")
        return ("india");
    if (vogal == "o")
        return ("oscar");
    if (vogal == "u")
        return ("uniform");
    if (vogal == "A")
        return ("alpha");
    if (vogal == "E")
        return ("echo");
    if (vogal == "I")
        return ("india");
    if (vogal == "O")
        return ("oscar");
    if (vogal == "U")
        return ("uniform");
    else {
        return (false);
    }
}