function vogalOuConsoante (letra){
    if (letra == "a")
        return ("Vogal");
    if (letra == "e")
        return ("Vogal");
    if (letra == "i")
        return ("Vogal");
    if (letra == "o")
        return ("Vogal");
    if (letra == "u")
        return ("Vogal");
    else {
        return ("Consoante");
    }
}